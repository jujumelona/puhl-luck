@dataclass
class CognitiveFieldSnapshot:
    """Complete snapshot of cognitive field state for serialization.
    
    Contains all four memory layers plus configuration and statistics.
    Used for saving and loading the entire cognitive field state.
    
    Requirement 1.6 specifies complete field state representation.
    """
    version: str
    timestamp: float
    
    # Layer 1: Exposure Events (preserved PUHL structure)
    events: Dict[str, Any]  # EventRecord instances (from original PUHL)
    edges: Dict[Tuple[int, int], float]
    feature_to_id: Dict[str, int]
    id_to_feature: List[str]
    event_hv: Dict[str, np.ndarray]
    
    # Layer 3: Operator Memory
    operators: Dict[str, OperatorRecord]
    
    # Layer 4: Transition Memory
    transitions: Dict[str, StateTransition]
    
    # Statistics
    total_exposures: int
    total_operators_induced: int
    total_transitions_stored: int
    
    # Configuration
    window_size: int
    decay: float
    hdc_dimensions: int


