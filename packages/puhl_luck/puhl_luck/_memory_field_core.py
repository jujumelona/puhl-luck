@dataclass
class StatePattern:
    """Abstract pattern that matches field states.
    
    Used by operators to determine when they are applicable to a given
    field state. Patterns can match based on features, concepts, 
    incompleteness markers, and active goals.
    
    Requirement 1.7 specifies operator storage with transformation patterns.
    """
    required_features: Set[str]
    required_concepts: Set[str]
    incompleteness_markers: List[str]
    goal_patterns: List[str]


@dataclass
class TransformationRule:
    """How to transform a matched state.
    
    Defines the transformation logic for converting partial states to
    complete states. Can be template-based, learned from transitions,
    or feature combination.
    
    Requirement 1.7 specifies transformation types within operators.
    """
    rule_type: str  # "template", "learned_transition", "feature_combination"
    parameters: Dict[str, Any]
    confidence_threshold: float


@dataclass
class OperatorRecord:
    """Learned state transformation operator (Layer 3).
    
    Represents a repeated transformation pattern abstracted from multiple
    exposures. Operators enable generalization beyond specific examples
    by capturing the structure of transformations.
    
    Requirements:
    - 1.7: Operator storage with types and transformation patterns
    - 4.1: Storage as state transformation operators
    - 4.2-4.6: Different operator types (explanation, repair, completion, etc.)
    """
    operator_id: str
    operator_type: OperatorType
    
    # Pattern matching
    pattern: StatePattern  # What field states this applies to
    preconditions: List[str]  # Required field properties
    
    # Transformation
    transformation: TransformationRule  # How to generate output
    completion_template: str
    
    # Statistics
    confidence: float
    usage_count: int
    success_rate: float
    generalization_level: int  # How abstract the operator is
    
    # Provenance
    induced_from: List[str]  # Transition IDs this was learned from
    timestamp: float


# ============================================================================
# Transition Memory Layer (Layer 4) Data Structures
# ============================================================================


@dataclass
class CompletionPattern:
    """Pattern of how a partial state was completed.
    
    Extracted from StateTransition, represents the abstract structure of
    completion that can be applied to similar partial states.
    
    Requirements:
    - 5.1-5.4: Partial-to-complete transitions across domains
    """
    # What was added to complete the state
    added_features: List[str]
    added_concepts: Set[str]
    
    # Completion characteristics
    completion_type: str  # "direct", "elaboration", "correction", "explanation"
    modality: str
    domain: str
    
    # HDC representation of completion
    completion_vector: np.ndarray
    
    # Confidence
    reliability: float


@dataclass
class StateTransition:
    """Partial-to-complete state transition (Layer 4).
    
    Stores a pair of (incomplete state, complete state) representing how
    the system learned to complete a particular type of incomplete state.
    NOT before/after pairs - specifically partial/complete pairs.
    
    Requirements:
    - 1.8: Storage of S_partial → S_complete pairs
    - 5.1: Transitions stored as S_partial → S_complete, not S_before → S_after
    - 5.2: Conversation turns as incomplete→completed context
    - 5.3: Code generation as incomplete→completed code
    - 5.4: Reasoning steps as incomplete→completed reasoning
    """
    transition_id: str
    
    # The incomplete → complete state pair
    partial_state: StateField
    complete_state: StateField
    
    # Difference representation
    completion_vector: np.ndarray  # HDC vector of what was added
    completion_features: List[str]
    
    # Context
    modality: str
    domain: str  # conversation, code, document, reasoning
    
    # Statistics
    timestamp: float
    relevance_count: int  # How often this transition was useful


# ============================================================================
# Persistence and Serialization Data Structures
