@dataclass
class InputContext:
    """Input context for field formation.
    
    Represents the input that triggers cognitive field activation across
    all memory layers. Used in Requirements 12.1 (query features), and
    supports optional goals and constraints for field dynamics.
    """
    query_text: str
    query_features: List[str] = None
    query_hv: np.ndarray = None
    modality: str = "text"
    domain: str = "general"
    partial_output: str = ""
    goals: Optional[List[str]] = None
    constraints: Optional[List[str]] = None
    metadata: Optional[Dict] = None
    
    def __post_init__(self):
        """Initialize optional fields with defaults."""
        if self.query_features is None:
            self.query_features = []
        if self.query_hv is None:
            self.query_hv = np.array([], dtype=np.uint64)
    
    @property
    def text(self) -> str:
        """Backward compatibility alias for query_text."""
        return self.query_text
    
    @classmethod
    def from_text(cls, text: str, **kwargs):
        """Create InputContext from text (backward compatibility)."""
        return cls(query_text=text, **kwargs)


@dataclass
class ConvergenceStats:
    """Statistics about convergence process.
    
    Tracks information about how and why the recursive stabilization
    loop converged, useful for analysis and debugging.
    """
    iterations: int
    final_energy: float
    energy_reduction: float
    convergence_reason: ConvergenceReason
    oscillation_detected: bool


@dataclass
class Candidate:
    """Generated output candidate that emerged from field tension.
    
    Represents a potential continuation that reduces field energy. Contains
    tracking information about what tension sources it addresses and how
    much energy reduction it provides.
    """
    # Content
    content: str
    tokens: List[str]
    
    # Energy analysis
    energy_reduction: float
    predicted_energy_after: float
    
    # Source tracking
    source: CandidateSource
    source_operators: List[str]  # Which operators contributed
    source_transitions: List[str]  # Which transitions contributed
    
    # Tension resolution
    tensions_addressed: List[TensionSource]
    tensions_resolved_count: int
    
    # Confidence
    confidence: float


