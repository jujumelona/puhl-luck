# State Field Layer (Layer 2) Data Structures
# ============================================================================


@dataclass
class ConflictMarker:
    """Marker for a conflict in the cognitive field.
    
    Conflicts increase field energy and represent incompatibilities between
    activated memories, constraint violations, or repetition patterns.
    Requirement 12.5 specifies conflict markers with strengths.
    """
    conflict_id: str
    conflict_type: ConflictType
    involved_memories: List[str]
    strength: float
    description: str


@dataclass
class GoalState:
    """Active goal in the cognitive field.
    
    Goals guide candidate generation by defining what the field should
    achieve. Unsatisfied goals create tension that drives output generation.
    Requirement 12.6 specifies goal states with satisfaction levels.
    """
    goal_id: str
    goal_description: str
    satisfaction_level: float  # 0.0 to 1.0
    constraints: List[str]


@dataclass
class TensionSource:
    """Source of tension in the cognitive field.
    
    Tension indicates high field energy from incompleteness, conflicts, or
    unsatisfied goals. Candidates are generated to reduce these tension sources.
    Used by candidate emergence to guide generation.
    """
    type: TensionType  # conflict, unsatisfied_goal, incomplete_output
    location: str  # Where in the field
    strength: float
    description: str


@dataclass
class StateField:
    """The currently activated cognitive field (Layer 2).
    
    Represents the living cognitive state containing simultaneously activated
    memories, concepts, operators, conflicts, goals, and partial outputs.
    This is the core data structure for field-based dynamics.
    
    Requirements:
    - 12.1: query feature representations
    - 12.2: activated events with strengths
    - 12.3: activated concepts with strengths
    - 12.4: activated operators with strengths
    - 12.5: conflict markers with strengths
    - 12.6: goal states with satisfaction levels
    - 12.7: partial output representations
    """
    # Input representation
    query_features: List[str]
    query_hv: np.ndarray
    
    # Activated memories with strengths
    activated_events: Dict[str, float]  # event_id → activation
    activated_concepts: Dict[str, float]  # concept_id → activation
    activated_operators: Dict[str, float]  # operator_id → activation
    
    # Field dynamics
    conflict_markers: List[ConflictMarker]
    goal_states: List[GoalState]
    partial_outputs: List[str]
    
    # Resonance matrix
    resonance: Dict[Tuple[str, str], float] = field(default_factory=dict)  # (mem1, mem2) → resonance_strength
    
    # Energy state
    field_energy: Optional[FieldEnergy] = None
    
    # History for recursive updates
    previous_outputs: List[str] = field(default_factory=list)
    iteration: int = 0


@dataclass
class FieldEnergy:
    """Field energy decomposition for free energy minimization.
    
    Energy = conflicts - evidence
    Lower energy is more stable. Field dynamics drive toward lower energy states.
    
    Evidence sources (decrease energy):
    - Memory resonance: Activated memories support each other
    - Goal satisfaction: Goals are being satisfied
    - Constraint satisfaction: Constraints are met
    - Memory support: Strong backing from memory
    - Coherence: Internal consistency
    
    Conflict sources (increase energy):
    - Contradiction: Memories conflict
    - Constraint violation: Constraints violated
    - Repetition: Unwanted repetition
    - Incompleteness: Missing information
    - Goal conflict: Goals conflict with each other
    
    Requirement 1.6 specifies field energy computation from all state components.
    """
    # Total energy (conflicts - evidence)
    total: float
    
    # Components
    evidence: float
    conflicts: float
    
    # Detailed breakdown
    evidence_breakdown: Dict[str, float]  # source → contribution
    conflict_breakdown: Dict[str, float]  # source → contribution
    
    # Interpretation
    dominant_evidence_sources: List[str]
    dominant_conflict_sources: List[str]
    tension_level: float  # Normalized 0-1


