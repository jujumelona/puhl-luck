"""Core data models and enums for Predictive Field Memory system.

This module implements the foundational data structures for the cognitive field-based
memory architecture, supporting the four-layer memory organization:
- Layer 1: Exposure Events Layer (raw observational data)
- Layer 2: State Field Layer (currently activated cognitive field)
- Layer 3: Operator Memory Layer (learned transformation patterns)
- Layer 4: Transition Memory Layer (partial-to-complete state transitions)

Requirements: 1.6, 12.1, 12.2, 12.3, 12.4, 12.5, 12.6, 12.7
"""

from __future__ import annotations

from dataclasses import dataclass, field
from enum import Enum
from typing import Any, Dict, List, Optional, Set, Tuple

import numpy as np


# ============================================================================
# Enums
# ============================================================================


class OperatorType(Enum):
    """Types of state transformation operators in Layer 3.
    
    Each operator type represents a different kind of transformation pattern
    that the system learns from experience.
    """
    COMPLETION = "completion"
    REPAIR = "repair"
    EXPLANATION = "explanation"
    COMPARISON = "comparison"
    TRANSFORMATION = "transformation"
    COMPOSITION = "composition"


class ConflictType(Enum):
    """Types of conflicts that can occur in the cognitive field.
    
    Conflicts increase field energy and drive candidate generation toward
    tension reduction.
    """
    CONTRADICTION = "contradiction"
    CONSTRAINT_VIOLATION = "constraint_violation"
    REPETITION = "repetition"


class TensionType(Enum):
    """Types of tension sources in the cognitive field.
    
    Tension indicates high field energy from incompleteness, conflicts,
    or unsatisfied goals. Candidate generation aims to reduce tension.
    """
    CONFLICT = "conflict"
    UNSATISFIED_GOAL = "unsatisfied_goal"
    INCOMPLETE_OUTPUT = "incomplete_output"


class CandidateSource(Enum):
    """Sources from which candidates can emerge.
    
    Candidates arise from different mechanisms in the field-based system:
    - Operator-based: Generated by applying learned operators
    - Transition-based: Generated from similar state transitions
    - Hybrid: Generated from combination of operators and transitions
    - Field dynamics: Generated directly from field resonance patterns
    """
    OPERATOR_BASED = "operator"
    TRANSITION_BASED = "transition"
    HYBRID = "hybrid"
    FIELD_DYNAMICS = "field_dynamics"


class ConvergenceReason(Enum):
    """Reasons why recursive stabilization converged or stopped.
    
    Indicates why the stabilization loop terminated, which helps diagnose
    generation quality and system behavior.
    """
    NOT_CONVERGED = "not_converged"
    ENERGY_MINIMUM = "energy_minimum"
    ENERGY_STABLE = "energy_stable"
    OUTPUT_COMPLETE = "output_complete"
    NO_CANDIDATES = "no_candidates"
    MAX_ITERATIONS = "max_iterations"


